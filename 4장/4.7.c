/*물리학에서 운동에너지는 E=mv^2/2.0으로 계산된다. 
사용자로부터 질량(m)과 속도(v)를 받아서 운동에너지(E)를 계산하는 프로그램을 작성하여 보자. 모든 변수는 double형을 사용하라.*/

#include <stdio.h>

int main(void)
{
    double m,v; //질량,속력

    printf("질량(kg):");
    scanf("%lf",&m);

    printf("속도(m/s):");
    scanf("%lf",&v);

    printf("운동에너지(J):%lf",0.5*m*v*v); //0.5*m*v^2

    return 0;
}