//정수형 변수에 대하여 실습하여보자. 

/*
(1) i의 값을 형식 지정자 %o, %x를 사용하여 8진수, 16진수로도 출력하여보라.
*/

#include <stdio.h>

int main(void)
{
    int i =255;
    printf("%d\n",i); //10진수
    printf("%o\n",i); // 8진수
    printf("%x",i); // 16진수

    return 0;
}
