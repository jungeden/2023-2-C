/*  사용자로부터 n을 입력받아서 조화 수의 합을 표시하는 프로그램을 C로 작성해보자. 
조화 수열은 각 항의 역수가 등차 수열을 이루는 수열로서 각 항은 점차 작아져서 0에 한없이 가까워지지만, 
그 역수의 합은 무한대가 된다고 알려져 있다. 사실일까? 컴퓨터로 천만개까지 계산해보자. */

// 1 + 1/2 + 1/3 + 1/4 + 1/5 + ... = ∞

#include <stdio.h>

int main(void){
    int a;
    double b;

    printf("항의 개수: "); scanf("%d",&a);

    for(int i=1;i<=a;i++){
        b+=1.0/i; //수열 합
    }
    printf("수열의 합 = %lf",b);

    return 0;
}