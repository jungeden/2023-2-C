//1부터 100 사이의 모든 3의 배수의 합을 계산하여 출력하는 프로그램을 반복 구조를 사용하여 작성 하라.

#include <stdio.h>

int main(){
    int a=0,i; 
    for(i=1;i<=100;i++){ //1부터 100까지 
        if(i%3==0) a=a+i; //3의 배수일 경우 더하기
    }
    printf("1부터 100 사이의 모든 3의 배수의 합은 %d입니다.",a);
    return 0;
}

