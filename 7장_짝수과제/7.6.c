//1^2 + 2^2 + 3^2 +...+n^2의 값을 계산하여 출력하여보자.

#include <stdio.h>

int main(void){
    int a,b=0;

    printf("n의 값을 입력하시오 :"); scanf("%d",&a);

    for(int i=1;i<=a;i++){ //1부터 입력값까지
        b+=i*i; // 제곱수 더하기
    }
    printf("계산값은 %d입니다.",b);
    return 0;
}