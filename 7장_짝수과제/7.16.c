/* 사용자가 입력한 특정한 정수의 자리수를 반대로 출력하는 프로그램을 작성하라. 
예를 들어서 사용자가 정수 1206을 입력하였다면 6021이 출력되어야 한다. do. while 문을 사용하여보라. */

#include <stdio.h>

int main(void){
    int a,b;
    printf("정수를 입력하시오: "); scanf("%d",&a);

    do
    {
        b=a%10; //????
        a/=10;
        printf("%d",b);
    } while (a!=0); //0이 아닐 경우 반복

    return 0;
}
    