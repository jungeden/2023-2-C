//사용자가 입력한 정수의 모든 약수를 화면에 출력하는 프로그램을 작성하라.

#include <stdio.h>

int main(void){
    int a;
    
    printf("정수를 입력하시오: "); scanf("%d",&a);
    printf("약수 :");

    for(int i=1;i<=a;i++){ //1부터 입력값까지
        if(a%i == 0) printf("%d ",i); //입력값과 나눴을 때 나머지가 0이면 약수
    }
    return 0;
}