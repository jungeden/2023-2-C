/*100보다 작은 정수를 입력받아서 이것을 십의 자리, 일의 자리로 분리하여 출력하는 프로그램을 작성하시오. 
즉 사용자가 정수 23을 입력하면 2,3을 차례로 출력하면 된다.*/

#include <stdio.h>

int main(void)
{
    int a;

    printf("정수를 입력하시오:");
    scanf("%d",&a);

    printf("십의 자리:%d\n일의 자리:%d",a/10,a%10); //10으로 나눠서 몫과 나머지로 각 자리 숫자 구하기

    return 0;
}