/* 구의 표면적과 체적을 구하는 프로그램을 작성하라. 
구의 반지름은 실수로 입력된다. 아래의 공식을 사용하라. 파이값은 기호 상수를 사용하여 정의하라. */

// A=4πr^2
// A=4/3πr^3

#include <stdio.h>
#define p 3.141592 //기호상수

int main(void)
{
    double r;

    printf("구의 반지름을 입력하시오 :");
    scanf("%lf",&r);

    printf("표면적은 %.2f입니다.\n체적은 %.2f입니다.",4*p*r*r,(4.0/3.0)*p*r*r*r); //공식

    return 0;
}
