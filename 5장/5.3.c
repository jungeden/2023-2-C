// 3개의 정수값을 입력받아서, 3개의 정수값 중에서 최대값을 출력하는 프로그램을 작성하라.

#include <stdio.h>

int main(void)
{
    int a,b,c,max;

    printf("정수 3개를 입력하시오 :");
    scanf("%d %d %d",&a,&b,&c);

    max=(a>b)?a:b; //조건 연산자 활용
    max=(max>c)?max:c;

    printf("최대값은 %d입니다.",max);

    return 0;
}