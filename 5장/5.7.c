/* 정수에 비트 연산자 <<를 한 번 적용하면 2를 곱한 값을 얻을 수 있다. 
또 정수에 비트 연산자 >>를 한 번 적용하면 2로 나눈 값을 얻을 수 있다.
사용자요부터 정수 x,y를 입력받아서 x<<y의 값을 출력하는 프로그램을 작성하여보자. */

#include <stdio.h>

int main(void)
{
    int a,b;

    printf("정수를 입력하시오 :");
    scanf("%d",&a);

    printf("2를 곱하고 싶은 횟수:");
    scanf("%d",&b);

    printf("%d<<%d의 값: %d",a,b,a<<b); //비트 이동 연산자

    return 0;
}