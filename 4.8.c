//사용자가 아스키 코드값을 입력하면 드 아스키 코드값에 해당하는 문자를 출력하는 프로그램을 작성하라.

#include <stdio.h>

int main(void)
{
    int a; 

    printf("아스키 코드값을 입력하시오:");
    scanf("%d",&a); //정수로 입력 받음

    printf("문자:%c입니다.",(char)a); //형변환

    return 0;
}