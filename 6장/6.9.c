/* 다음과 같이 정의되는 함수의 함수값을 계산하여보자. 
사용자로부터 x값을 입력받아서 함수값을 계 산하여 화면에 출력한다. x는 실수이다. */

#include <stdio.h>

int main(void)
{
    double x,y;

    printf("x의 값을 입력하시오 :"); scanf("%lf",&x);
    
    if(x<=0) y=x*x-9*x+2; //0보다 작거나 같은 경우
    else y=7*x+2; //0보다 큰 경우

    printf("f(x)의 값은 %.2f입니다.",y);

    return 0;
}