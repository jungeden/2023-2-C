#include <stdio.h>

int main(void){
    int a=1,n,s=0;
    do
    {
        printf("숫자를 입력하시오: "); scanf("%d",&n);
        if (n<0) continue; //음수면 합계에서 제외
        else {
            s+=n; //양수면 더하고
            a+=1; //카운트 값 올리기
            }

    } while (a<4); //사용자에게 정수 3개 받을 것이기 때문에 카운트 값 3이하
    return 0;
}